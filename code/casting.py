# working with casting 
# casting is used to convert a datatype to another datatype 

number_of_cars = "23"
amount_per_car = 4000

# casting number_of_cars to int. 
sale = int(number_of_cars) * amount_per_car
print(sale)


float_sale = float(sale)


print(float_sale) # printing sale 
print(type(float_sale)) # printing the datatype 

number = 23
str_num = str(number) # casting an int to string  

# printing the type 
print(type(number)) 
print(type(str_num))